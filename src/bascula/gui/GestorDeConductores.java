    @Override
    public void EventoDeSeleccionDeConductor() {
        this.AsignarConductor(bc.getSeleccionado());
        this.jButton1.setEnabled(false);
        this.jButton3.setEnabled(true);
        this.jButton4.setEnabled(true);
        this.show();
        bc.dispose();
    }

    @Override
    public void AsignarConductor(Conductor c) {
        this.conductor=c;
        if(c!=null){
            this.jFormattedTextField1.setValue(c.getIdConductor());
            this.jTextField1.setText(c.getNombres());
            this.jTextField2.setText(c.getApellidos());
            this.jCheckBox1.setSelected(c.getReportado());
        }else{
            this.jFormattedTextField1.setValue(new Long(0));
            this.jTextField1.setText("");
            this.jTextField2.setText("");
            this.jCheckBox1.setSelected(false);
        }
    }
