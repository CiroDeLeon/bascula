    public void Ingresar(){
        conductor=new bascula.entity.Conductor();
        conductor.setIdConductor((Long)this.jFormattedTextField1.getValue());
        conductor.setNombres(this.jTextField1.getText());
        conductor.setApellidos(this.jTextField2.getText());
        conductor.setReportado(this.jCheckBox1.isSelected());
        try{
        service.getController().create(conductor);
        JOptionPane.showMessageDialog(this,"Conductor Ingresado Con Exito");
        }catch(bascula.controller.exceptions.PreexistingEntityException e){
            System.out.print(e.getMessage());
            JOptionPane.showMessageDialog(this,"Ese Conductor Ya Existe");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"ERROR Hubo una Excepcion NO SE PUDO INGRESAR");
            Logger.getLogger(GestorDeConductores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void Modificar(){     
        conductor.setIdConductor((Long)this.jFormattedTextField1.getValue());
        conductor.setNombres(this.jTextField1.getText());
        conductor.setApellidos(this.jTextField2.getText());
        conductor.setReportado(this.jCheckBox1.isSelected());
        try{
        service.getController().edit(conductor);
        JOptionPane.showMessageDialog(this,"Conductor Modificado Con Exito");
        this.AsignarConductor(null);
        this.jButton1.setEnabled(true);
        this.jButton3.setEnabled(false);
        this.jButton4.setEnabled(false);
        }catch(bascula.controller.exceptions.NonexistentEntityException e){
            System.out.print(e.getMessage());
            JOptionPane.showMessageDialog(this,"Ese Conductor NO Existe");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"ERROR Hubo una Excepcion NO SE PUDO Modificar");
            Logger.getLogger(GestorDeConductores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void Eliminar(){     
        conductor.setIdConductor((Long)this.jFormattedTextField1.getValue());
        conductor.setNombres(this.jTextField1.getText());
        conductor.setApellidos(this.jTextField2.getText());
        conductor.setReportado(this.jCheckBox1.isSelected());
        try{
        service.getController().destroy(conductor.getIdConductor());
        JOptionPane.showMessageDialog(this,"Conductor Eliminado Con Exito");
        this.AsignarConductor(null);
        this.jButton1.setEnabled(true);
        this.jButton3.setEnabled(false);
        this.jButton4.setEnabled(false);
        }catch(bascula.controller.exceptions.NonexistentEntityException e){
            System.out.print(e.getMessage());
            JOptionPane.showMessageDialog(this,"Ese Conductor NO Existe");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"ERROR Hubo una Excepcion NO SE PUDO Eliminar");
            Logger.getLogger(GestorDeConductores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

